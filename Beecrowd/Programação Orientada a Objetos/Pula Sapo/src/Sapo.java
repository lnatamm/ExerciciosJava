public class Sapo {

    private int tamanhoPulo;

    public Sapo(){

        this.tamanhoPulo = 0;

    }

    public Sapo(int tamanhoPulo){

        this.tamanhoPulo = tamanhoPulo;

    }

    public int getTamanhoPulo(){

        return this.tamanhoPulo;

    }

    public void setTamanhoPulo(int tamanhoPulo){

        this.tamanhoPulo = tamanhoPulo;

    }

}
